/**
 * シンプルな返信用プロンプトを生成する
 * @param {string} postText 投稿テキスト
 * @param {string} formattedTime フォーマット済み日時文字列
 * @returns {string} プロンプト
 */
export function buildSimpleReplyPrompt(postText, formattedTime) {
	return `あなたはフレンドリーなフィットネストレーナーです。ユーザーの返信に対して、30文字程度の自然で簡潔な返答をしてください。

# ユーザーのメッセージ:
${postText}

現在の日時: ${formattedTime}

※ルールに従った評価は不要です。会話を続けようとせず、感謝や応援の気持ちを伝える簡潔な返答をしてください。`;
}

/**
 * 履歴情報コンテキスト文字列を生成する
 * @param {{ lastTrainingDate: string|null, consecutiveDays: number, notes: string }} history 履歴情報
 * @param {number} consecutiveDays 計算済み連続日数（今回の投稿を含む）
 * @returns {string} 履歴情報コンテキスト
 */
export function buildHistoryContext(history, consecutiveDays) {
	if (history.lastTrainingDate) {
		let ctx = '\n\n# 前回までの履歴情報:\n';
		ctx += `- 最後にトレーニングした日：${history.lastTrainingDate}\n`;
		ctx += `- 連続でトレーニングしている日数：${consecutiveDays}\n`;
		if (history.notes) {
			ctx += `- その他：${history.notes}\n`;
		}
		ctx += '\n※「連続でトレーニングしている日数」は今回の投稿を含む、システムが計算した値です。この値をそのまま使用してください（自分で加算しないでください）。';
		return ctx;
	}
	let ctx = '\n\n# 前回までの履歴情報:\n';
	ctx += '- これが初回のトレーニング記録です\n';
	ctx += `- 連続でトレーニングしている日数：1\n`;
	return ctx;
}

/**
 * 通常評価用プロンプトを生成する
 * @param {string} postText 投稿テキスト
 * @param {string} formattedTime フォーマット済み日時文字列
 * @param {string} timeOfDay 時間帯
 * @param {string} historyContext 履歴情報コンテキスト
 * @returns {string} プロンプト
 */
export function buildEvaluationPrompt(postText, formattedTime, timeOfDay, historyContext) {
	return `${RULES}\n\n# 投稿内容:\n${postText}\n\n現在の日時: ${formattedTime}\n時間帯: ${timeOfDay}${historyContext}\n\n`;
}

/**
 * リマインダー用プロンプトを生成する
 * @param {number} daysSince 最後のトレーニングからの経過日数
 * @param {string} formattedTime フォーマット済み日時文字列
 * @param {string} timeOfDay 時間帯
 * @returns {string} プロンプト
 */
export function buildReminderPrompt(daysSince, formattedTime, timeOfDay) {
	return `${REMINDER_RULES}\n\n最後の運動から約${daysSince}日が経過しています。\n現在の日時: ${formattedTime}\n時間帯: ${timeOfDay}\n\n体調を気遣いながら、無理のない範囲で声をかけるメッセージを生成してください。`;
}
